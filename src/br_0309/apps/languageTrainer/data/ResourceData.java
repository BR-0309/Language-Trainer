package br_0309.apps.languageTrainer.data;

public class ResourceData {

    public final String file;
    public final String author;
    public final String license;
    public final String notes;
    public final String link;

    public ResourceData(String file, String author, String license, String notes, String link) {
        this.file = file; this.author = author; this.license = license; this.notes = notes; this.link = link;
    }

}
